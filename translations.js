// ===== دوال المساعدة =====

function t(key) {
  const lang = localStorage.getItem('lang') || 'ar';
  return translations[lang]?.[key] || key;
}

function setLanguage(lang) {
  localStorage.setItem('lang', lang);
  location.reload();
}

function getCurrentLang() {
  return localStorage.getItem('lang') || 'ar';
}

function translateRole(roleName) {
  const roleMap = {
    "ذئب": t('role_wolf'),
    "قروي": t('role_villager'),
    "المستذئب": t('role_infected'),
    "الصياد": t('role_hunter'),
    "المنقذ": t('role_savior'),
    "المستبصرة": t('role_seer'),
    "الساحرة": t('role_witch'),
    "الذئب الصغير": t('role_little_wolf'),
    "رجل حرائق": t('role_fireman'),
    "الغراب": t('role_raven'),
    "الغبي": t('role_fool'),
    "البومة": t('role_owl'),
    "باربي": t('role_barbie')
  };
  return roleMap[roleName] || roleName;
}

function translateDynamicRole(roleObj) {
  const lang = getCurrentLang();
  if (typeof roleObj === 'string') return roleObj;
  if (roleObj && typeof roleObj === 'object') {
    return roleObj[lang] || roleObj.ar || 'دور غير معروف';
  }
  return 'دور غير معروف';
}

